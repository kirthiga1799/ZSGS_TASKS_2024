package com.tasks;

public class Conversion_1 {

	    public static void main(String[] args) {
	    	
	        // Float to int conversion
	        float floatNumber = 10.5f;
	        int intNumber = (int) floatNumber;
	        System.out.println("Float to int conversion:");
	        System.out.println("float number: " + floatNumber);
	        System.out.println("int number: " + intNumber);
	        System.out.println();

	        // Double to float conversion
	        double doubleNumber = 20.75f;
	        float convertedFloat = (float) doubleNumber;
	        System.out.println("Double to float conversion:");
	        System.out.println("double number: " + doubleNumber);
	        System.out.println("float number: " + convertedFloat);
	        System.out.println();

	        // Int to short conversion
	        int intValue = 1000;
	        short shortValue = (short) intValue;
	        System.out.println("Int to short conversion:");
	        System.out.println("int value: " + intValue);
	        System.out.println("short value: " + shortValue);
	    }
	}


